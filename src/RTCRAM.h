#ifndef RTC_RAM_H_
#define RTC_RAM_H_

#include <stdint.h>

#define RTC_RAM_SIZE          512

// RTC RAM data
typedef struct {
    uint16_t wakeCount;
} RTCRamData;

static_assert(sizeof(RTCRamData) <= RTC_RAM_SIZE, "RTC RAM size exceeded");

void readRTCRam();
void writeRTCRam();

#endif // RTC_RAM_H_
